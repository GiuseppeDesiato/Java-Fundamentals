package labs_examples.datatypes_operators;

public class float_double {

    public static void main(String[] args) {

        float x = 1234.123F; // the float number needs and F at the end of it,
                             // otherwise the editor gives an error. because Java assumes that that number is a double
        double y = 1234.123;

        System.out.println(x);
        System.out.println(y);

    }
}
